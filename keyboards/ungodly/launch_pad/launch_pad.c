// Potentiometer Slider, MIDI Control
uint8_t divisor = 0;
void slider(void) {
    if (divisor++) { // only run the slider function 1/256 times it's called
        return;
    }
    midi_send_cc(&midi_device, 2, 0x3E, 0x7F - (analogReadPin(SLIDER_PIN) >> 3));
}
void matrix_scan_user(void) {
    slider();
}

